function serendipity(ξ,η)
    N = Array{Operation}(undef, 8);

    N[1] = -(1/4) * (1-ξ) * (1-η) * (1+ξ+η);
    N[2] = -(1/4) * (1+ξ) * (1-η) * (1-ξ+η);
    N[3] = -(1/4) * (1+ξ) * (1+η) * (1-ξ-η);
    N[4] = -(1/4) * (1-ξ) * (1+η) * (1+ξ-η);
    N[5] = (1/2) * (1-ξ^2) * (1-η);
    N[6] = (1/2) * (1+ξ) * (1-η^2);
    N[7] = (1/2) * (1-ξ^2) * (1+η);
    N[8] = (1/2) * (1-ξ) * (1-η^2);

    dNξ = Array{Operation}(undef, 8);

    dNξ[1] = (1/4) * (1-η) * (2*ξ+η);
    dNξ[2] = (1/4) * (1-η) * (2*ξ-η);
    dNξ[3] = (1/4) * (1+η) * (2*ξ+η);
    dNξ[4] = (1/4) * (1+η) * (2*ξ-η);
    dNξ[5] = -ξ * (1-η);
    dNξ[6] = (1/2) * (1-η^2);
    dNξ[7] = -ξ * (1+η);
    dNξ[8] = -(1/2) * (1-η^2);

    dNη = Array{Operation}(undef, 8);

    dNη[1] = (1/4) * (1-ξ) * (ξ+2*η);
    dNη[2] = -(1/4) * (1+ξ) * (ξ-2*η);
    dNη[3] = (1/4) * (1+ξ) * (ξ+2*η);
    dNη[4] = -(1/4) * (1-ξ) * (ξ-2*η);
    dNη[5] = -(1/2) * (1-ξ^2);
    dNη[6] = -(1+ξ) * η;
    dNη[7] = (1/2) * (1-ξ^2);
    dNη[8] = -(1-ξ) * η;

    return N, dNξ, dNη
end